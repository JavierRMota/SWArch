# The +HttpStatus+ class is an 
# object containing the constants
# for each useful HTTP response.
class HttpStatus
    # OK constant
    OK = 200
    # CREATED constant
    CREATED = 201
    # ACCEPTED constant
    ACCEPTED = 202
    # BAD_REQUEST constant
    BAD_REQUEST = 400
    # METHOD_NOT_ALLOWED constant
    METHOD_NOT_ALLOWED = 405
    # NOT_FOUND constant
    NOT_FOUND = 404
    # ERROR constant
    ERROR = 500
end
